package org.example.week4.d3.e6;

public class E6DoWhileLoop {
    public static void main(String[] args) {

        int counter = 20;   // Step 1: start from 20

        do {
            System.out.println(counter);  // print number
            counter -= 2;   // decrease by 2
        } while (counter >= 2);   // loop condition

    }
}
