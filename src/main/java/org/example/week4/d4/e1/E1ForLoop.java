package org.example.week4.d4.e1;

public class E1ForLoop {
    public static void main(String[] args) {
        for (int i = 2; i <= 14; i++) {   // start at 2, stop at 14
            if (i % 2 == 0) {             // check if even
                System.out.println(i);
            }
        }

    }
}
